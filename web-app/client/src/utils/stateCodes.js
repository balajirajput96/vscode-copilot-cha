/**
 * Get USPS state code from state name
 * @param {string} stateName - Full state name
 * @returns {string} Two-letter USPS state code or empty string if not found
 */
export const getStateCode = (stateName) => {
  if (!stateName || typeof stateName !== 'string') {
    return '';
  }
  
  // Normalize input: trim whitespace, handle multiple spaces, and convert to title case
  const normalized = stateName.trim()
    .replace(/\s+/g, ' ')
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(' ');
  
  return STATE_CODES[normalized] || '';
};

/**
 * Get all state names
 * @returns {string[]} Array of state names
 */
export const getStateNames = () => {
  return Object.keys(STATE_CODES);
};
